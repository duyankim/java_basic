package sw06;

public class Calc1 {
	/* 메소드 오버로딩 2021-04-27 kopo03 김도연 */
	
	public int k03_sum(int k03_a, int k03_b) {
		return k03_a + k03_b;
	}

	public int k03_sum(int k03_a, int k03_b, int k03_c) {
		return k03_a + k03_b + k03_c;
	}

	public int k03_sum(int k03_a, int k03_b, int k03_c, int k03_d) {
		return k03_a + k03_b + k03_c + k03_d;
	}

	public double k03_sum(double k03_a, double k03_b) {
		return k03_a + k03_b;
	}
}

