package sw2;

public class P21 {

	public static void main(String[] args) {
		/* SpecialCharEx 2021-04-26 kopo03 김도연 */
		System.out.println('\'');			// '를 출력하기 위해 \를 앞에 붙임
		System.out.println("abc\t123\b456");// \t는 tab을, \b는 단어 사이의 구분점
		System.out.println('\n');			// 개행문자 \n
		System.out.println("\"Hello\"");	// \"라고 써야 "가 나온다.
		System.out.println("c:\\");			// \를 나타내기 위해선 \\를 쓴다.
	}

}
